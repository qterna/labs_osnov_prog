#include <stdio.h>

int main() {
    double a, b, h;

    // Ввод оснований и высоты трапеции
    printf("Введите длины оснований трапеции (a, b) и её высоту (h): ");
    scanf("%lf %lf %lf", &a, &b, &h);

    // Вычисляем и выводим площадь
    printf("Площадь трапеции: %.2f\n", ((a + b) / 2.0) * h);

    return 0;
}
